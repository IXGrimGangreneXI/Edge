package org.asf.edge.mmoserver.entities.coordinates;

public class Quaternion4D {

	public double x;
	public double y;
	public double z;
	public double w;

	public Quaternion4D() {
	}

	public Quaternion4D(double x, double y, double z, double w) {
		this.x = x;
		this.y = y;
		this.z = z;
		this.w = w;
	}
}
